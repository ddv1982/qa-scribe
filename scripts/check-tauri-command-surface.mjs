import { readdirSync, readFileSync } from 'node:fs'
import { basename, join } from 'node:path'
import process from 'node:process'

const root = process.cwd()

const buildSource = read('src-tauri/build.rs')
const spectaSource = read('src-tauri/src/specta_bindings.rs')
const bindingsSource = read('frontend/src/bindings.ts')
const permissionSource = read('src-tauri/permissions/default.toml')
const capabilitySource = JSON.parse(read('src-tauri/capabilities/default.json'))
const expectedCapabilityPermissions = [
  'core:event:allow-listen',
  'core:event:allow-unlisten',
  'core:window:allow-destroy',
  'default',
].sort()

const buildBlock = requiredMatch(buildSource, /const COMMANDS: &\[&str\] = &\[([\s\S]*?)\];/, 'build.rs COMMANDS')[1]
const buildCommands = matches(buildBlock, /"([a-z0-9_]+)"/g)

const spectaBlock = requiredMatch(spectaSource, /\.commands\(collect_commands!\[([\s\S]*?)\]\)/, 'Specta collect_commands')[1]
const spectaCommands = matches(spectaBlock, /commands(?:::[a-z0-9_]+)+::([a-z0-9_]+),/g)

const bindingCommands = matches(bindingsSource, /__TAURI_INVOKE<[^>]+>\("([a-z0-9_]+)"/g)
const defaultPermissions = matches(permissionSource, /"allow-([a-z0-9-]+)"/g).map((name) => name.replaceAll('-', '_'))
const generatedPermissions = readdirSync(join(root, 'src-tauri/permissions/autogenerated'))
  .filter((file) => file.endsWith('.toml'))
  .map((file) => basename(file, '.toml'))

const surfaces = {
  'build manifest': buildCommands,
  'Specta handler': spectaCommands,
  'generated bindings': bindingCommands,
  'default permission set': defaultPermissions,
  'autogenerated permissions': generatedPermissions,
}

const expected = sortedUnique(buildCommands, 'build manifest')
for (const [label, commands] of Object.entries(surfaces)) {
  const actual = sortedUnique(commands, label)
  if (!sameValues(expected, actual)) {
    const missing = expected.filter((command) => !actual.includes(command))
    const extra = actual.filter((command) => !expected.includes(command))
    fail(`${label} differs from build manifest\n  missing: ${missing.join(', ') || 'none'}\n  extra: ${extra.join(', ') || 'none'}`)
  }
}

if (!Array.isArray(capabilitySource.permissions)) {
  fail('main-window capability must declare its permissions')
}
const capabilityPermissions = sortedUnique(capabilitySource.permissions, 'main-window capability permissions')
if (!sameValues(expectedCapabilityPermissions, capabilityPermissions)) {
  const missing = expectedCapabilityPermissions.filter((permission) => !capabilityPermissions.includes(permission))
  const extra = capabilityPermissions.filter((permission) => !expectedCapabilityPermissions.includes(permission))
  fail(`main-window capability permissions differ\n  missing: ${missing.join(', ') || 'none'}\n  extra: ${extra.join(', ') || 'none'}`)
}
if (!Array.isArray(capabilitySource.windows) || !capabilitySource.windows.includes('main')) {
  fail('default capability must be scoped to the main window')
}

console.log(`tauri command surface: OK — ${expected.length} commands and the main-window core permissions agree`)

function read(path) {
  return readFileSync(join(root, path), 'utf8')
}

function requiredMatch(value, pattern, label) {
  const match = value.match(pattern)
  if (!match) fail(`could not locate ${label}`)
  return match
}

function matches(value, pattern) {
  return Array.from(value.matchAll(pattern), (match) => match[1])
}

function sortedUnique(values, label) {
  const unique = [...new Set(values)].sort()
  if (unique.length !== values.length) fail(`${label} contains duplicate commands`)
  return unique
}

function sameValues(left, right) {
  return left.length === right.length && left.every((value, index) => value === right[index])
}

function fail(message) {
  console.error(`tauri command surface: FAILED — ${message}`)
  process.exit(1)
}
